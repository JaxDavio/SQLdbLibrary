FROM BOOK AS B INNER JOIN
BOOK_COPIES AS BC 
ON B.BookID = BC.BookID
INNER JOIN LIBRARY_BRANCH AS LB
ON BC.BranchID = LB.BranchID
WHERE B.Title = 'The Lost Tribe'




