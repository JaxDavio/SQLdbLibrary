SELECT BO.CardNo, BO.Name, BO.[Address], BC.NoOfCopies
FROM BORROWER AS BO INNER JOIN
BOOK_LOANS AS BL
ON BO.CardNo = BL.CardNo
INNER JOIN BOOK_COPIES AS BC
ON BC.BranchID = BL.BranchID
WHERE BO.CardNo < 1003




